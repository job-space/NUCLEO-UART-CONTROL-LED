/* Function to clear spaces and newline characters */
void TrimString(char *str)
{
    int length = strlen(str);
    int start = 0;

    // Clear the beginning of the line (spaces and newline characters)
    while (str[start] == ' ' || str[start] == '\n' || str[start] == '\r')
    {
        start++;
    }

    // Clear the end of the line
    int end = length - 1;
    while (end >= start && (str[end] == ' ' || str[end] == '\n' || str[end] == '\r'))
    {
        end--;
    }

    // Move the cleared line to the beginning
    for (int i = 0; i <= end - start; i++)
    {
        str[i] = str[start + i];
    }
    str[end - start + 1] = '\0'; // End the line
}

void Blink_LED(uint32_t delay)
{
	UART_TransmitString("LED is blink\r\n");
    while (1)
    {
    	HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);  // Switching the LED status
    	HAL_Delay(500);  // Delay of 500 ms

    }
}
/* Function for processing UART commands */
void Process_UART_Command(char *command)
{
    if (strcmp(command, "blink") == 0)
    {
        // LED flashing at a speed of 500 ms
        Blink_LED(500);
    }
    else if (strcmp(command, "on") == 0)
    {
        // Turn on the LED
        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
        UART_TransmitString("LED is ON\r\n");
    }
    else if (strcmp(command, "off") == 0)
    {
        // Turn off the LED
        HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
        UART_TransmitString("LED is OFF\r\n");
    }
    else
    {
        // Unknown command
        UART_TransmitString("Invalid command!\r\n");
    }
}

/* Transferring a string via UART */
void UART_TransmitString(char *string)
{
    HAL_UART_Transmit(&huart2, (uint8_t *)string, strlen(string), HAL_MAX_DELAY);
}
